// Point class for storing Co-ordinates of the starting point of the shape
public class Point 
{
  private double x,y;
  public Point(double x,double y)
  {
        this.x= x;
        this.y=y;
  }
// getter method for getting X-co-ordinate
 public double getX()
    {
        return this.x;
    }
//getter method for getting Y-co-ordinate
 public double getY()
    {
        return this.y;
    }
}
